<p><strong>Eli…</strong></p>

<p>Je t’aime hyper fort, plus que quiconque. <br>
J’étais la petite chose apeurée que tu as recueillie et que tu chérissais depuis maintenant plus d’un an et demi. Maintenant je suis un petit fur, apeuré par la pensée de perdre.</p>

<p>Je voudrais te montrer, chaque jour, à quel point je t’adore. A quel point tu es précieux et important pour moi. Je voudrais vivre avec toi et être toujours là pour toi. <br>
J’aurais souhaité que tu vienne me parler quand tu avais besoin, j’aurais toujours répondu le plus vite possible. Je voulais être là pour t’écouter, mais aussi pour t’aider, te soutenir, te conseiller ou même m’adapter, si le problème était lié à nous.</p>

<p>J’aurais voulu être plus souvent là pour toi. Combattre la distance et parler plus avec toi. J’essayais de prendre des initiatives, de t’inciter à venir parler en vocal avec moi, en TS, souvent en vain. <br>
Finalement, j’avais plus peur de te déranger qu’autre chose. Alors quand tu me répondais “<em>désolé, je taffe</em>“, je te câlinais comme je pouvais, et te souhaitais bonne chance, en rappelant quelques fois que j’étais toujours là pour toi, si tu souhaitais parler quand même.</p>

<p>Alors chaque jour, je pensais à toi. Je me demandais comment tu allais, etc.. Mais à distance, si tu ne me parle pas de ce qui ne va pas, je ne peux pas deviner ce que tu penses ou ressens..</p>

<p><strong>Tu es tout pour moi</strong> et tu l’a toujours été. </p>

<p>Cette dernière année passée ensemble, notre relation s’est énormément intensifié, et je m’attachais de plus en plus vite. Les séjours passaient et je devenais de plus en plus accro à ta compagnie. Tellement qu’au final, j’étais triste de te voir t’éloigner de moi à nouveau. Il ne me suffisait que d’être à peine quelques jours de nouveau loin de toi pour que tu ne me manque encore.</p>

<p>Tout ce je faisais de mon côté, au final, je le faisais aussi pour toi. J’avais envie de continuer, même si peu de gens ne s’intéresse à mes projets, car tu me rendais fier de mes créations. D’autres choses était directement influencées par toi : mes dessins par exemples, qui, pour la plupart, te sont destinés, et où les autres me servait d’entraînement.</p>

<p>Mais aujourd’hui, tu es encore plus loin de moi qu’auparavant. Alors, maintenant, je me bats pour me tenir debout, et continuer tant bien que mal mes projets, tel que tu le souhaitais, mais j’en souffre. <br>
Je vis sous le stress en ce moment et j’ai peur de beaucoup de chose. La rentrée scolaire n’aide pas, entre perte de confiance et peur pour mon avenir, ou même simplement ma fin d’année.</p>

<p>— <em>Mati, pour Eli.</em> <br>
<small><em>le 7 Septembre 2016.</em></small></p>

